package jmt.framework.gui.listeners;

import java.awt.event.ComponentEvent;
import java.awt.event.FocusEvent;
import java.awt.event.FocusListener;
import java.awt.event.KeyEvent;
import java.awt.event.KeyListener;

/**
 * <p><b>Name:</b> KeyFocusAdapter</p> 
 * <p><b>Description:</b> 
 * An action listener used to update a data structure when a component loses focus or 
 * ENTER key is pressed.
 * </p>
 * <p><b>Date:</b> 23/gen/07
 * <b>Time:</b> 18:00:06</p>
 * @author Bertoli Marco
 * @version 1.0
 */
public abstract class KeyFocusAdapter implements KeyListener, FocusListener {

	/**
	 * Performs an action when components lose focus or ENTER key is typed.
	 * @param e event that triggered the listener
	 */
	protected abstract void updateValues(ComponentEvent e);

	/* (non-Javadoc)
	 * @see java.awt.event.FocusListener#focusGained(java.awt.event.FocusEvent)
	 */
	public void focusGained(FocusEvent e) {
	}

	/* (non-Javadoc)
	 * @see java.awt.event.FocusListener#focusLost(java.awt.event.FocusEvent)
	 */
	public void focusLost(FocusEvent e) {
		updateValues(e);
	}

	/* (non-Javadoc)
	 * @see java.awt.event.KeyListener#keyPressed(java.awt.event.KeyEvent)
	 */
	public void keyPressed(KeyEvent e) {
		if (e.getKeyCode() == KeyEvent.VK_ENTER) {
			updateValues(e);
			e.consume();
		}
	}

	/* (non-Javadoc)
	 * @see java.awt.event.KeyListener#keyReleased(java.awt.event.KeyEvent)
	 */
	public void keyReleased(KeyEvent e) {
	}

	/* (non-Javadoc)
	 * @see java.awt.event.KeyListener#keyTyped(java.awt.event.KeyEvent)
	 */
	public void keyTyped(KeyEvent e) {
	}
}
